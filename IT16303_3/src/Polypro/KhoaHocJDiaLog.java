    void delete() {
        if (MgsBox.confirm(this, "Bạn thực sự muốn xóa khóa học này?")) {
            Integer makh = Integer.valueOf(cboChuyenDe.getToolTipText()); //maKH để nhờ ở toolTipText
            try {
                dao.delete(makh);
                this.fillTable();
                this.clear();
                MgsBox.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                MgsBox.alert(this, "Xóa thất bại!");
            }
        }
    }

