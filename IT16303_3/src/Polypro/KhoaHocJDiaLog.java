    int row=-1;
    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblGridView.getModel();
        model.setRowCount(0);
        try {
            List<khoaHoc> list = dao.selectAll();
            for (khoaHoc kh : list) {
                Object[] row = {
                    kh.getMaKH(),
                    kh.getMaCD(),
                    kh.getThoiLuong(),
                    kh.getHocPhi(),
                    XDate.toString(kh.getNgayKG()),
                    kh.getMaNV(),
                    XDate.toString(kh.getNgayTao())
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MgsBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void insert() {
        khoaHoc model = getModel();
        model.setNgayTao(XDate.now());
        try {
            dao.insert(model);
            this.fillTable();
            this.clear();
            MgsBox.alert(this, "Thêm mới thành công!");
        } catch (HeadlessException e) {
            MgsBox.alert(this, "Thêm mới thất bại!");
        }
    }

